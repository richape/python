# Integer (whole number)
age = 39

# Floating-point number (decimal)
height = 1.81

# String (text)
name = "Richard"

print(age, height, name) # This prints output: age, height, name

# Basic Operations
a = 10
b = 5

# Addition
print(a + b) # Output: 15

# Subtraction
print(a - b) # Output: 5

# Multiplication
print(a * b) # Output: 50

# Division
print(a / b) # Output: 2

# if statements (Conditionals)
age = 39

if age >= 18:
  print("You are an adult")
else:
  print("You are still a child")

# Loops
# Example loop
for i in range(5):
  print(i)

# Example while loop
x = 0
while x < 5:
  print(x)
  x += 1

# Functions
def greet(name):
  print("Hello, " + name)

  greet("Richard") # Output: Hello, Richard